package day4_ControlStatements;

public class NumberIsPositiveNegativeOrZero {

	public static void main(String[] args) {
		int num=4 ;
		if(num>0) {
			System.out.println("Positive Number");
		}
		else if(num<0){
			System.out.println("Negative Number");
		}
		else {
			System.out.println("Zero");
		}

	}

}
